package com.lukaszstachnik;

public class Right_ascension {
    public int getXx() {
        return xx;
    }

    public void setXx(int xx) {
        this.xx = xx;
    }

    public int getYy() {
        return yy;
    }

    public void setYy(int yy) {
        this.yy = yy;
    }

    public int getZz() {
        return zz;
    }

    public void setZz(int zz) {
        this.zz = zz;
    }

    private int xx;
    private int yy;
    private int zz;

    Right_ascension(int xx, int yy, int zz){
        this.xx = xx;
        this.yy = yy;
        this.zz = zz;
    }


}
